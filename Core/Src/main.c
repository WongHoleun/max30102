// 串口中断回调函数
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart) {
	if (huart->Instance == USART1) {
		if (0xAA == Receive_Flag) {
			Flag = true;	// 置位 传感器启动标志
			Receive_Flag = 0;	// 复位 接收标志
			HAL_TIM_Base_Start_IT(&htim11);	// 在中断方式下启动定时器 11（单次记时间1s）
		}
		HAL_UART_Receive_IT(huart, &Receive_Flag, 1);	// 重新使能接收中断，准备下一次接收
	}
}

// 定时器更新中断回调函数
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim) {
	if (htim -> Instance == TIM11)	//判断发送更新中断的定时器
	{
		timer_counts++;
	}
	if (60 == timer_counts)			// 达到60s， 1分钟
	{
		__HAL_TIM_CLEAR_IT(&htim11, TIM_IT_UPDATE);	// 清除时钟更新中断标志
		HAL_TIM_Base_Stop_IT(&htim11);
		timer_counts = 0;
		Flag = false;
	}
}
